-- ---------------------------------------------------------------------------
-- Пометить исторические авто-нарушения («Авто из оценки») как исключённые
-- (legacy), чтобы они НЕ попадали в текущие расчёты нарушений/штрафов.
--
-- Контекст: раньше при сохранении оценки «Критично» платформа автоматически
-- создавала нарушение (note = 'авто из оценки'). Эти строки писались с
-- confirmed = true и потому считались подтверждёнными ручными нарушениями
-- Маргариты. Автосоздание удалено; нарушения вносит только Маргарита вручную.
--
-- Мы НЕ удаляем строки (сохраняем историю/трассируемость), а помечаем
-- confirmed = false — весь код отчётов/дашборда/сообщений уже исключает
-- нарушения с confirmed = false. Ручные нарушения не затрагиваются.
--
-- Идемпотентно: повторный запуск ничего не меняет.
-- ---------------------------------------------------------------------------
update mqa_violations
set confirmed = false
where note = 'авто из оценки'
  and confirmed is distinct from false;

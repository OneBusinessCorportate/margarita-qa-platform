-- Задачу можно назначать не только бухгалтеру, но и менеджеру (запрос QA:
-- «Также задачу необходимо назначать не только бухгалтеру, но и менеджеру»).
--
-- WHY
-- ---
--   Раньше у задачи было единственное поле исполнителя — accountant. Менеджер
--   по клиенту уже хранится в mqa_chats.manager, но задачу нельзя было назначить
--   на него. Добавляем параллельную текстовую колонку manager (как у
--   mqa_chats.manager — тот же пул имён сотрудников, без FK).
--
-- Аддитивно и обратно совместимо: у старых задач manager = NULL. Идемпотентно.

alter table mqa_tasks add column if not exists manager text;
comment on column mqa_tasks.manager is
  'Ответственный менеджер по задаче (дополнительно к accountant). Тот же пул имён, что и бухгалтеры.';

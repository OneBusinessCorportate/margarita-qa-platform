-- (см. updateChatAccountant в src/lib/repo.ts).
--
-- Сразу закрепляем 5 чатов, переназначенных миграцией
-- 20260713_mqa_new_accountants_and_reassign.sql (Марианна / Артур / Алиса),
-- чтобы следующий синк их не вернул на Лилит / Сатеник.
-- «В-4370» — с кириллической В (chr(1042)), как реальный ключ в базе.
-- ---------------------------------------------------------------------------
alter table mqa_chats
  add column if not exists accountant_pinned boolean not null default false;

update mqa_chats set accountant_pinned = true
  where agr_no in ('B-3862', 'B-4376', 'B-4273', '180', chr(1042) || '-4370');

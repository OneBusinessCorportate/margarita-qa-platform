-- Приводим mqa_accountants к утверждённому списку из 14 действующих бухгалтеров
-- (см. src/lib/valid-employees.ts). До этой миграции в общей БД оставались
-- помечены active сотрудники, которых в списке быть не должно (уволенные, чужие
-- отделы, «служебные» значения, вторая Гаяне), а двух валидных сотрудников
-- (Դավիթ уже был, Ռոբերտ уже был) не хватало по грейдам, а Արթուր отсутствовал.
--
-- ВАЖНО: приложение уже фильтрует бухгалтеров через valid-employees на уровне
-- кода (listAccountants + ReportView), поэтому дашборд корректен и без этой
-- миграции. Миграция лишь приводит саму таблицу в порядок. Ключ mqa_accountants
-- (name) — это FK для mqa_chats.accountant, поэтому НЕ удаляем строки (можно
-- порвать ссылки на исторические чаты), а только помечаем active=false. Свои
-- клиентские чаты при необходимости переназначаются отдельно.
--
-- Idempotent: безопасно повторять.

-- 1) Деактивируем всех, кого нет в утверждённом списке (роль accountant).
update mqa_accountants
   set active = false
 where role = 'accountant'
   and name not in (
     'Գայանե', 'Օլյա', 'Ստելլա', 'Նաիրա', 'Արփինե', 'Թագուհի', 'Ավագ',
     'Նաիրա Մ․', 'Հասմիկ', 'Լիլիթ', 'Դավիթ', 'Ռոբերտ', 'Լիլիթ Ք․', 'Արթուր'
   );

-- 2) Добавляем недостающих валидных сотрудников (если их ещё нет).
insert into mqa_accountants (name, active, role) values
  ('Արփինե', true, 'accountant'),
  ('Դավիթ',  true, 'accountant'),
  ('Ռոբերտ', true, 'accountant'),
  ('Արթուր', true, 'accountant')
on conflict (name) do update set active = true, role = 'accountant';

-- 3) Гарантируем, что все 14 валидных активны (на случай прежней деактивации).
update mqa_accountants
   set active = true
 where name in (
   'Գայանե', 'Օլյա', 'Ստելլա', 'Նաիրա', 'Արփինե', 'Թագուհի', 'Ավագ',
   'Նաիրա Մ․', 'Հասմիկ', 'Լիլիթ', 'Դավիթ', 'Ռոբերտ', 'Լիլիթ Ք․', 'Արթուր'
 );
